package teste;

public class Exercicio2 {
	// casting 
	public static void main(String[] Args) {
		int a, b;
		double resultado;
		a =5;
		b= 2;
		resultado = (double )a / b;
		System.out.println(resultado);
		// casting é a conversão explicita dos valores, ex: de inteiro para double
		
		double z;
		int d;
		z = 5f;
		d = (int) z;
		System.out.println(d);
	}
	
}
